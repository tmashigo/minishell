#include "libft.h"

char	*ft_strrev(char *s)
{
	char	temp;
	int		i;
	int		j;

	j = strlen(s);
	i = 0;
	while (i < (j - 1))
	{
		temp = s[i];
		s[i] = s[j - 1];
		s[j - 1] = temp;
		j--;
		i++;
	}
	return (s);
}
